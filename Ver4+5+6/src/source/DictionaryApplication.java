    private void tab3_translate_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tab3_translate_buttonActionPerformed
        String selected = String.valueOf(tab3_selected.getSelectedItem());        
        //System.out.println(selected);
        if (selected == "English to Vietnamese") {
            tab3_target_textArea.setLineWrap(true);
            tab3_target_textArea.setWrapStyleWord(true);
            String targetString = tab3_target_textArea.getText();
            try {
                String explainString =  dicM.translate("en", "vi", targetString);
                tab3_explain_textArea.setText(explainString);
                //tu xuong dong
                tab3_explain_textArea.setLineWrap(true);
                tab3_explain_textArea.setWrapStyleWord(true);
            } catch (IOException ex) {
                Logger.getLogger(DictionaryApplication.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        else {            
            String targetString = tab3_target_textArea.getText();
            try {
                String explainString =  dicM.translate("vi", "en", targetString);
                tab3_explain_textArea.setText(explainString);
            } catch (IOException ex) {
                Logger.getLogger(DictionaryApplication.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tab3_translate_buttonActionPerformed

    private void tab1_target_textFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tab1_target_textFieldKeyReleased
        String search = tab1_target_textField.getText();  
        
        DefaultListModel model = new DefaultListModel();
        ArrayList <Word> listSearch = new ArrayList<Word>();
        listSearch = dicM.dictionarySearcher(search);
        for (int i = 0; i < listSearch.size(); i++) {
            model.addElement(listSearch.get(i).getWord_target());
        }
        tab1_suggest_list.setModel(model);
        tab1_explain_textArea.setText("");
    }//GEN-LAST:event_tab1_target_textFieldKeyReleased

