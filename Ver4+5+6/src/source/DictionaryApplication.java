    private void tab2_delete_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tab2_delete_buttonActionPerformed
        String check_explain_textArea = tab2_explain_textArea.getText();
        
        if (!check_explain_textArea.equals("")) {
        
            String deleteTarget = tab2_target_textField.getText();
            boolean check = dicM.deleteWord(deleteTarget);   
            
            message_lable.setText("Done!");
            message_dialog.setLocationRelativeTo(null);
            message_dialog.setVisible(true);

            //xoa tu vua xoa
            tab2_target_textField.setText("");
            tab2_explain_textArea.setText("");     

            DefaultListModel model = new DefaultListModel();             
            tab2_suggest_list.setModel(model);

        }
        else {
            message_lable.setText("Choose word to delete!");
            message_dialog.setLocationRelativeTo(null);
            message_dialog.setVisible(true);
            
        }
    }//GEN-LAST:event_tab2_delete_buttonActionPerformed

